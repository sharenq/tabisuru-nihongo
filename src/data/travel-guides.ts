export function applyTravelGuides(regions: readonly Region[]): Region[] {
  return regions.map((region) => ({
    ...region,
    prefectures: region.prefectures.map((prefecture) => {
      const guide = travelGuideData[prefecture.id];
      if (guide) {
        return { ...prefecture, travelGuide: guide };
      }
      return prefecture;
    }),
  }));
}
